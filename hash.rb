{ "tokyo" => 13163345, "kanagawa" => 9145572}
# 上記と同じでハッシュロケットの代わりにコロンを使っている
{ "tokyo": => 13163345, "kanagawa": => 9145572}
# シンボルをキーにしている
{ :tokyo => 13163345, :kanagawa => 9145572 }
# 上記と同じ意味で、コロンを使っている（最も使われている）
{ tokyo: 13163345, kanagawa: 9145572 }
# 数字をキーにしている
{ 13 => 13163345, 14 => 9145572 }